def start_end_discounts_2025_new_year(testrun=True, start_or_end="end"):
    client = RohseoulClient()
    variants = [client.variant_by_sku(sku) for sku in skus]

    if start_or_end == "end":
        client.revert_variant_prices(variants=variants, testrun=testrun)
    else:
        new_prices_by_variant_id = {
            v["id"]: int(int(v["compareAtPrice"] or v["price"]) * 0.9) for v in variants
        }
        client.update_variant_prices_by_dict(
            variants=variants,
            new_prices_by_variant_id=new_prices_by_variant_id,
            testrun=testrun,
        )
